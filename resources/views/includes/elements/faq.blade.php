<script>
    document.addEventListener('DOMContentLoaded', function() {
        const faqItems = document.querySelectorAll('.faq-item');

        faqItems.forEach(item => {
            const question = item.querySelector('.faq-question');
            const answer = item.querySelector('.faq-answer');
            const toggle = item.querySelector('.faq-toggle svg');

            question.addEventListener('click', function() {
                const isActive = item.classList.contains('active');

                // Закрываем все остальные элементы
                faqItems.forEach(otherItem => {
                    if (otherItem !== item) {
                        otherItem.classList.remove('active');
                        const otherAnswer = otherItem.querySelector('.faq-answer');
                        const otherToggle = otherItem.querySelector('.faq-toggle svg');
                        otherAnswer.style.maxHeight = '0';
                        otherToggle.classList.remove('opened_svg');
                    }
                });

                // Переключаем текущий элемент
                if (isActive) {
                    item.classList.remove('active');
                    answer.style.maxHeight = '0';
                    toggle.classList.remove('opened_svg');
                } else {
                    item.classList.add('active');
                    answer.style.maxHeight = answer.scrollHeight + 'px';
                    toggle.classList.add('opened_svg');
                }
            });
        });
    });
</script>
